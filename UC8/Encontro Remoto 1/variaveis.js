//variáveis

//var, let, const

//var : Escopo global - Podem ser redeclaradas, a última variável declarada sobrescreve a primeira com o mesmo nome
var x = 0;
var x = 'Renato';
console.log(x);

//let : Escopo local - Não podem ser redeclaradas, mas sim alteradas
let y = 0;
// let y = 'Carlos';
console.log(y);

let nome = 'Renan'
console.log(nome)
nome = 'José'
console.log(nome)

//const : Não pode ser redeclarada e nem alterada
const ano = '2022'
console.log(ano)
